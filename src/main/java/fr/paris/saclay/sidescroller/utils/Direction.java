package fr.paris.saclay.sidescroller.utils;

/**
 * Defines direction for each entity.
 */
public enum Direction {
    /**
     * Up left direction.
     */
    UP_LEFT,
    /**
     * Up right direction.
     */
    UP_RIGHT,
    /**
     * Left direction.
     */
    LEFT,
    /**
     * Right direction.
     */
    RIGHT,
    /**
     * Attack left direction.
     */
    ATTACK_LEFT,
    /**
     * Attack right direction.
     */
    ATTACK_RIGHT,
    /**
     * Block left direction.
     */
    BLOCK_LEFT,
    /**
     * Block right direction.
     */
    BLOCK_RIGHT
}
